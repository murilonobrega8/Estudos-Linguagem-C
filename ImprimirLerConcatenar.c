void main() {

    //Definindo uma variável
    int a = 5;

    //Imprimindo a variável "a"
    printf("%d", a);

    //Concatenando
    printf("O valor da variável 'a' é: %d", a);

    //Pula linha
    printf("\n\n");

    //Mudando o valor de "a"
    a = a+10;

    //Concatenando
    printf("O valor da variável 'a' somado a 10 é: %d", a);

    //Imprimindo
    printf("\nUhuuu");

    //Pausando
    system("pause");    

}